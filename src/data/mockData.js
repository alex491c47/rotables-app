export function fmtMoney(n) {
  if (!n) return "$0";
  if (n >= 1e6) return "$" + (n / 1e6).toFixed(2).replace(/\.?0+$/, "") + "M";
  return "$" + Math.round(n / 1e3) + "K";
}

export function fmtDays(n) {
  if (!n) return "—";
  if (n >= 365) { const y = (n / 365); return y.toFixed(y >= 10 ? 0 : 1) + " yr"; }
  return n + " d";
}
